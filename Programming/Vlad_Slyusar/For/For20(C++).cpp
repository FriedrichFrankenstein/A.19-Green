#include <stdio.h>
#include <conio.h>
#include <math.h>


int main ()
{ 
	int i, number, summ, n, res ;
	printf ("Enter number: ");
	scanf  ("%i", &number);
	for (i = 1; i < number; i++){
		for (i; i > n; n++ ){
			res *= i ;
		}
		summ += res;
		
	}
	printf ("Summ = %i", summ);
	getchar();
	return 0;
}
